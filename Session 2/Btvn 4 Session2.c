#include <stdio.h>
int main(){
	//Bien canh hinh vuong
	int squareEdge = 4;
	
	//Tich chu vi hinh vuong
	int Perimeter;
	Perimeter = squareEdge * 4;
	printf ("Chu vi hinh vuong la %d ", Perimeter);
	
	//Tinh dien tich hinh vuong
	int Acreage;
	Acreage = squareEdge * squareEdge;
	printf ("\nDien tich hinh vuong la %d", Acreage);
	
	 
	return 0;
}
